Es gibt diverse Methodologien der Softwareentwicklung, die sich zum Teil fundamental unterscheiden.<br/> 
In einem Punkt aber sind sich alle "Propheten" der Softwaretechnik einig:<br/>
Zu Begin jeder Softwarentwicklung sollte geklärt werden, <b>was</b> denn eigentlich 
entwickelt werden soll. <br/>
Diesem Zweck dient die Anforderungsanalyse, in der u.a. folgende Fragen geklärt werden:
<ul>
	<li>Was muss das System leisten?</li>
	<ul>
		<li>Welche Eingaben erwartet das System?</li>
		<li>Wie erfolgt die Eingabe?</li>
		<li>Welche Daten soll das System verarbeiten können?</li>
		<li>Müssen Daten dauerhaft gespeichert werden?</li>
		<li>Welche Berechnungen soll das System durchführen?</li>
		<li>Welche Ausgaben tätigt das System?</li>
	</ul>
	<li>In welchen Kontext muss das System eingebunden werden?</li>
	<ul>
		<li>Vorhandene Software</li>
		<li>Vorhandene Hardware</li>
		<li>Altdatenbestand</li>
		<li>gewohnte Arbeitsabläufe</li>
	</ul>
</ul>