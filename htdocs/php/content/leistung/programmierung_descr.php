Detail aufgebaut sein soll, gilt es bei der Programmierung diese theoretischen Überlegungen in die Tat 
umzusetzen.<br/>
<p>Welche Programmiersprache zum Einsatz kommen soll, wird beim Systementwurf festgelegt. Bei Softwaresystemen, 
die aus mehreren Komponenten und/oder Schichten bestehen, kommen häufig mehrere Sprachen zum Einsatz.</>
<p>Im Verlauf meiner mehr als 10-jährigen Berufspraxis habe ich in einer ganzen Reihe unterschiedlichster Sprachen 
programmiert (siehe <a href="index.php?page=programmierung&content=/php/content/leistung/programmierung_sprachen.php">Sprachen</a>). 
Die besten Erfahrungen habe ich dabei mit der sog. "<em>objektorienten Programmierung</em>" insbesondere 
in <strong>Java</strong> gemacht.</p>