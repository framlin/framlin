Nachdem in der Anforderungsanalyse die Funktionen des Systems und der Projektumfang grob abgesteckt wurden, 
geht es beim Systementwurf darum, weitere Details zu bestimmen und zu klären, 
<strong>wie</strong> das System realisiert werden soll.<br/>
Wenn die Methodologie es erfordert (bei schwergewichtigen Softwarentwicklungsprozessen), 
wird außerdem eine Planung erstellt.<br/>
Im Rahmen des Systementwurfs werden u.a folgende Fragen geklärt:
<ul>
	<li>Welche Technologie kommt zum Einsatz?
		<ul>
			<li>Welche Programmiersprachen werden verwendet?</li>
			<li>Können bestehende Frameworks und/oder Bibliotheken eingestzt werden?</li>
			<li>Existieren bereits fertige Systemkomponenten, die eingebunden werden können?</li>
		</ul>
	</li>
	<li>Welche Architektur soll das System haben?
		<ul>
			<li>wie viele Schichten?</li>
			<li>welche Entwurfsmuster?</li>
			<li>verteiltes oder monolytisches System?</li>
			<li>Einzel- oder Mehrplatzsystem?</li>
			<li>Web- oder Desktopapplikation?</li>
		</ul>
	</li>
	<li>Können quelloffene Systemkomponenten verwendet werden?</li>
	<li>Wie sind die Rollen und Zuständigkeiten der am Entwicklungsprozess beteiligten Personen verteilt?</li>
	<li>Welche Meilensteine sieht der Zeitplan vor?</li>
	<li>Welche Kosten fallen an?</li>
</ul>